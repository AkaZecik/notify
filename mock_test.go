func (mfs FS) Dump() (root string, err error) {
	if root, err = ioutil.TempDir("", "notify"); err != nil {
		return
	}
	if n := fsutil.Copy(mfs.Filesystem, fsutil.Rel(fs.Default, root)); n != mfs.int {
		err = fmt.Errorf("MockedFilesystem.Dump() failure: want n=%d; got %d", mfs.int, n)
	}
	return
}

