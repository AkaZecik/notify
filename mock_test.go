func count(fs fs.Filesystem) (n int) {
	fs.Walk(sep, func(_ string, _ os.FileInfo, err error) error {
		if err != nil {
			panic(err)
		}
		n++
		return nil
	})
	n-- // Root dir (/) does not count.
	return
}

